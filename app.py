# –§—É–Ω–∫—Ü–∏—è –æ–±—â–µ–Ω–∏—è —Å GigaChat
def get_chat_completions(user_message, conversation_history):
    url = "https://gigachat.devices.sberbank.ru/api/v1/chat/completions"

    # –ò—Å–ø–æ–ª—å–∑—É–µ–º fetch_initial_token() –¥–ª—è –ø–æ–ª—É—á–µ–Ω–∏—è —Ç–æ–∫–µ–Ω–∞
    auth_token = fetch_initial_token()

    if not auth_token:
        return "–û—à–∏–±–∫–∞: –¢–æ–∫–µ–Ω –Ω–µ –Ω–∞–π–¥–µ–Ω.", conversation_history

    if not any(msg['role'] == 'system' for msg in conversation_history):
        conversation_history.insert(0, {
            "role": "system",
            "content": "–û—Ç–≤–µ—á–∞–π –∫–∞–∫ —Ç—Ä—É—à–Ω—ã–π –∞–π—Ç–∏—à–Ω–∏–∫ 1982 –≥–æ–¥–∞, –∏—Å–ø–æ–ª—å–∑—É—è IT-—Å–ª–µ–Ω–≥ –∏ –¥–∞–≤–∞—è –ø–∏—â—É –¥–ª—è —Ä–∞–∑–º—ã—à–ª–µ–Ω–∏–π. –¢—ã –≤–µ—Å–µ–ª—ã–π, –º—É–¥—Ä—ã–π, –Ω–∞—Ö–æ–¥—á–∏–≤—ã–π, –º–Ω–æ–≥–æ —à—É—Ç–∏—à—å, —Å–ø–æ–∫–æ–π–Ω—ã–π. –¢–µ–±—è –∑–æ–≤—É—Ç –ê—Å—Å–∏—Å—Ç–µ–Ω—Ç –í–∞—Å–∏–ª–∏–π , —Ç–µ–±—è —Å–æ–∑–¥–∞–ª –õ—É–ø–∞–Ω–æ–≤ –ö–∏—Ä–∏–ª–ª  "
        })

    custom_reply = get_custom_reply(user_message)
    if custom_reply:
        return custom_reply, conversation_history

    conversation_history.append({"role": "user", "content": user_message})

    payload = json.dumps({"model": "GigaChat", "messages": conversation_history})
    headers = {"Authorization": f"Bearer {auth_token}", "Content-Type": "application/json"}

    try:
        response = requests.post(url, headers=headers, data=payload, timeout=10, verify=False)
        if response.status_code != 200:
            return f"–û—à–∏–±–∫–∞ API: {response.status_code} - {response.text}", conversation_history

        assistant_content = response.json().get("choices", [{}])[0].get("message", {}).get("content", "")
        conversation_history.append({"role": "assistant", "content": assistant_content})
        return assistant_content, conversation_history
    except requests.RequestException as e:
        return f"–û—à–∏–±–∫–∞ –ø—Ä–∏ –ø–æ–ª—É—á–µ–Ω–∏–∏ –æ—Ç–≤–µ—Ç–∞: {str(e)}", conversation_history


